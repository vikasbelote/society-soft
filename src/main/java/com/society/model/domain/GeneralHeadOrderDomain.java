package com.society.model.domain;

public class GeneralHeadOrderDomain {
	
	private Integer orderId;
	
	private Integer configId;
	
	private Integer generalHeadId;
	
	private Integer sequnceNumber;

	public Integer getOrderId() {
		return orderId;
	}

	public void setOrderId(Integer orderId) {
		this.orderId = orderId;
	}

	public Integer getConfigId() {
		return configId;
	}

	public void setConfigId(Integer configId) {
		this.configId = configId;
	}

	public Integer getGeneralHeadId() {
		return generalHeadId;
	}

	public void setGeneralHeadId(Integer generalHeadId) {
		this.generalHeadId = generalHeadId;
	}

	public Integer getSequnceNumber() {
		return sequnceNumber;
	}

	public void setSequnceNumber(Integer sequnceNumber) {
		this.sequnceNumber = sequnceNumber;
	}
}
